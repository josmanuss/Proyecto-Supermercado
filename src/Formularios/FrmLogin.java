    private void JBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAceptarActionPerformed
        Sesion.getInstance().usuario = this.tFUsuario.getText();
        Sesion.getInstance().contrasenia = this.pFContrasenia.getText();
        Trabajador tra = supe.getlT().buscarUsuarioContrasenia(Sesion.getInstance().usuario, Sesion.getInstance().contrasenia);
        if (Sesion.getInstance().usuario.isEmpty() && Sesion.getInstance().contrasenia.isEmpty()){
            JOptionPane.showMessageDialog(this, "Campos vacios", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
        else if ( intentos == 3 ){
            JOptionPane.showMessageDialog(this, "Se cerrara el programa", "",JOptionPane.INFORMATION_MESSAGE);
            System.exit(0);
        }
        else{
            if ( tra == null ){
                JOptionPane.showMessageDialog(this, "No existe el trabajador\n Te quedan "+(3-intentos)+
                        " intentos","Error",JOptionPane.ERROR_MESSAGE);
                this.tFUsuario.setText("");
                this.pFContrasenia.setText("");
                this.tFUsuario.requestFocus();
                this.intentos++;
            }
            else{
                JOptionPane.showMessageDialog(this, "Bienvenido al sistema: "+tra.getNombre()+" "+tra.getApellidoPaterno(),"Mensaje de bienvenida",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                FrmVentanaPrincipal frVP = new FrmVentanaPrincipal(tra);
                frVP.setSup(this.supe);
                frVP.llenarComboBox();
                frVP.show();
            }
        }
       this.tFUsuario.setText("");
       this.pFContrasenia.setText("");
    }//GEN-LAST:event_JBAceptarActionPerformed

