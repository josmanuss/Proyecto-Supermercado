    private void tFUsuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tFUsuarioKeyReleased
        String userInput = tFUsuario.getText();

        SwingUtilities.invokeLater(() -> {
            NodoTrabajador nodo = supe.getlT().getCab();
            while (nodo != null) {
                if (userInput.equals(nodo.getTra().getUsuario())) {
                    String contrasenia = nodo.getTra().getContrasenia();
                    SwingUtilities.invokeLater(() -> pFContrasenia.setText(contrasenia));
                    break; // Detener el bucle después de encontrar una coincidencia
                }
                nodo = nodo.getSgte();
            }
            pFContrasenia.setText("");
        });
    }//GEN-LAST:event_tFUsuarioKeyReleased
   
    private List<String> getUsuariosCompletions() {
        List<String> completions = new ArrayList<>();
        NodoTrabajador aux = this.supe.getlT().getCab();
        while (aux != null) {
            completions.add(aux.getTra().getUsuario());
            aux = aux.getSgte();
        }
        return completions;
    }
    
    public void llenadoComboBoxUsuario(){
        comboBoxUsuario.removeAllItems();
        List<String> usuarios = getUsuariosCompletions();
        for ( int i = 0; i < usuarios.size(); i++ ){
            comboBoxUsuario.addItem(usuarios.get(i));
        }
    }   
    
    public void llenadoListener(){
        
        tFUsuario.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                updateComboBox();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateComboBox();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                updateComboBox();
            }
        });
        
    }
    
    private void updateComboBox() {
        String userInput = tFUsuario.getText();
        comboBoxUsuario.getEditor().setItem(userInput);
        completions = getUsuariosCompletions();
        comboBoxUsuario.removeAllItems();
        for (String user : completions ) {
            if (user.startsWith(userInput)) {
                comboBoxUsuario.addItem(user);
            }
        }
        if ( comboBoxUsuario.getItemCount() > 0 ){
            comboBoxUsuario.setPopupVisible(true);
        }
        else{
            comboBoxUsuario.setPopupVisible(false);
        }
        
        comboBoxUsuario.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Object selectedItem = comboBoxUsuario.getSelectedItem();
                if (selectedItem != null) {
                   
                    tFUsuario.setText(selectedItem.toString());
                    
                }
            }
        });
        
     }
    
